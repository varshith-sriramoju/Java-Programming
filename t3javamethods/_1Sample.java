package t3javamethods;

public class _1Sample {
    public static void printNumbers1To10() {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }

    public static void printNumbers20To40() {
        for (int j = 20; j <= 40; j++) {
            System.out.println(j);
        }
    }

    public static void printNumbersFrom(int start, int end) {
        for (int k = start; k <= end; k++) {
            System.out.println(k);
        }
    }
}
